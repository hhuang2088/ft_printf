#include "ft_printf.h"

int		is_flag(const char flag)
{
  int	i;
  char	*flags;

  i = 0;
  flags = "-0+# ";
  while (flags[i])
  {
	if (flag == flags[i])
	  return(1);
	i++;
  }
  return (0);
}
